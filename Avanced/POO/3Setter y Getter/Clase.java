public class Clase{

   private int edad;
   private String nombre;
   
   //		Metodo setter
   public void setEdad(int edad){
      this.edad = edad;
   }
   
   //		Metodo getter
   public int getEdad(){
      return edad;
   }

   
   public void setNombre(String nombre){
      this.nombre = nombre;
   }
   
   public String getNombre(){
      return nombre;
   }
   
}